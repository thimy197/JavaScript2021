/**
 * Math Object
 * - Math.PI
 * - Math.round() : làm tròn số
 * - Math.abs() : lấy giá trị tuyệt đối
 * - Math.ceil() : Làm tròn trên, 1,001=2
 * - Math.floor() : Làm tròn dưới 1,999=1
 * - Math.random() : luôn trả về 1 số 0<value<1
 *      Math.floor(Math.random()*10); Random số từ 0->9
 * - Math.min() : Tìm số nhỏ nhất trong dãy số
 *      Math.min(-100, 99, 2, 43, 66); ==> -100
 * - Math.max() : Tìm số lớn nhất trong dãy số
 */